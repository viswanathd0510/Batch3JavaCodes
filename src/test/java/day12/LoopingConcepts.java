package day12;

public class LoopingConcepts {

	public static void main(String[] args) {

		
		for (int i = 1; i<=100; i++)  {
			
			System.out.println(i);
		}
		
		System.out.println("************************************");
		
		for (int c=100; c>=1; c -- ) {
			
			System.out.println(c);
		}
		
	}

}
