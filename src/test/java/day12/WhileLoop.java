package day12;

public class WhileLoop {

	public static void main(String[] args) {

		// Initializing
		// Condition
		// Increment 
		
		int i = 1;
		int e = 10 ;
		while(i<=e)	
		{
			System.out.println(i);	
			i++;
		}
		
		System.out.println("****************************");
		
		int o = 500 ;
		int c = 400 ;
		while(o>=c)
		{
			System.out.println(o);
			o--;
		}
		
		System.out.println("****************************");	
	}
	}

