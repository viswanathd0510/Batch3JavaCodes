package day10;

public class StringManipulation {

	public static void main(String[] args) {

		String s = "Core Java" ;
		// While we count 	123456789
				int i = s.length();
				System.out.println(i);
				//Char at 
				// Indexing 
				
				// "Core Java"
				//	012345678
				char c = s.charAt(3);
				System.out.println(c);
				
				// substring 
				System.out.println(s.substring(5));
				System.out.println(s.substring(0, 6));
	}

}
